using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _5tocompu_get_y_set.clases
{
    class operaciones
    {
        public int sum(int a, int b)
        {
            int r;
            r = a + b;
            return r;
        }
        public int res(int a, int b)
        {
            int r;
            r = a - b;
            return r;
        }
        public int mul(int a, int b)
        {
            int r;
            r = a * b;
            return r;
        }
        public double div(double a, double b)
        {
            double r;
            r = a / b;
            return r;
        }
    }
}
